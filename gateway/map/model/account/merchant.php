class ModelAccountMerchant extends Model {

    public function getMerchants($data = array()) {
        $sql = "SELECT * FROM `" . DB_PREFIX . "merchant`";

        $sort_data = array(
            'legal_name',
            'status',
            'date_added'
        );

        if (isset($data['sort']) && in_array($data['sort'], $sort_data)) {
            $sql .= " ORDER BY " . $data['sort'];
        } else {
            $sql .= " ORDER BY legal_name";
        }

        if (isset($data['order']) && ($data['order'] == 'DESC')) {
            $sql .= " DESC";
        } else {
            $sql .= " ASC";
        }

        if (isset($data['start']) || isset($data['limit'])) {
            if ($data['start'] < 0) {
                $data['start'] = 0;
            }

            if ($data['limit'] < 1) {
                $data['limit'] = 20;
            }

            $sql .= " LIMIT " . (int)$data['start'] . "," . (int)$data['limit'];
        }

        $query = $this->db->query($sql);

        return $query->rows;
    }

    public function getTotalMerchants() {
        $query = $this->db->query("SELECT COUNT(*) AS total FROM `" . DB_PREFIX . "merchant`");

        return $query->row['total'];
    }
}
